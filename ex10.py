# The \t is indenting the line in the console
tabby_cat = "\tI'm tabbed in."
# \n is splitting the line in half
persian_cat = "I'm split\non a line."
# the \\ just forms a \
backlash_cat = "I'm \\ a \\ cat."
# the \t is indenting each line
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""
# regular print
print(tabby_cat)
print(persian_cat)
print(backlash_cat)
print(fat_cat)